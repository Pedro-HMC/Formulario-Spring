package com.programem.formulario;

public class Formulario {
    private String nome;
    private String idade;

    public String getNome(){
        return this.nome;
    }

    public void setNome(String nome){
        this.nome = nome;
    }
    
    public String getIdade(){
        return this.idade;
    }

    public void setIdade(String idade){
        this.idade = idade;
    }

}